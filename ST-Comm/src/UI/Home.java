    private void playGameActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_playGameActionPerformed
        if(chooseGame.getSelectedItem()==null){
            JOptionPane.showMessageDialog(null, "Please Choose Game !", "ST-Comm", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(chooseCategory.getSelectedItem()==null){
            JOptionPane.showMessageDialog(null, "Please Choose Category !", "ST-Comm", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (type.equals("Student")) {
            if (!GameController.isUnlocked(chooseGame.getSelectedItem().toString(), username)) {
                JOptionPane.showMessageDialog(null, "This Game is Locked As Your Level is Less than Game Level\n"
                        + "Play more games to increase your Level !", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        try {

            int score = GameController.playGame(chooseGame.getSelectedItem().toString());
            if (type.equals("Student")) {
                StudentController.saveScore(username, score, chooseGame.getSelectedItem().toString());
            }
            JOptionPane.showMessageDialog(null, "The Game Finished !\nYour Score is " + score, "ST-Comm", JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.toString(), "ST-Comm", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_playGameActionPerformed

