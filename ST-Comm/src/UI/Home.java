    public static int displayQuestion(Question question, int numOfQuestions, int idx) {
        JLabel statement = new JLabel(question.getQuestionStatement());
        JPanel jp = new JPanel();
        jp.setLayout(new GridLayout(6, 5, 10, 10));
        jp.add(statement);
        if (question.getType() == QuestionType.TRUEorFALSE) {
            ButtonGroup bg = new ButtonGroup();
            JLabel a2 = new JLabel("");
            JRadioButton r1 = new JRadioButton("True");
            JRadioButton r2 = new JRadioButton("False");
            r1.setSelected(true);

            bg.add(r1);
            bg.add(r2);
            jp.add(r1);
            jp.add(r2);

            int result = JOptionPane.showConfirmDialog(null, jp,
                    "Question " + (idx + 1) + " Out Of " + numOfQuestions, JOptionPane.OK_CANCEL_OPTION);
            if (result != JOptionPane.OK_OPTION) {
                return -1;
            }
            int answer = 0;
            if (r1.isSelected()) {
                answer = 0;
            } else if (r2.isSelected()) {
                answer = 1;
            }
            JOptionPane.showMessageDialog(null, "Correct Answer is ' " + question.getChoices().get(question.getCorrectAnswer()) + " '", "ST-Comm", JOptionPane.INFORMATION_MESSAGE);

            return answer;
        } else {
            ButtonGroup bg = new ButtonGroup();
            JLabel a2 = new JLabel("");
            JRadioButton r1 = new JRadioButton(question.getChoices().get(0));
            JRadioButton r2 = new JRadioButton(question.getChoices().get(1));
            JRadioButton r3 = new JRadioButton(question.getChoices().get(2));
            JRadioButton r4 = new JRadioButton(question.getChoices().get(3));
            r1.setSelected(true);
            bg.add(r1);
            bg.add(r2);
            bg.add(r3);
            bg.add(r4);
            jp.add(r1);
            jp.add(r2);
            jp.add(r3);
            jp.add(r4);
            int result = JOptionPane.showConfirmDialog(null, jp,
                    "Question " + (idx + 1) + " Out Of " + numOfQuestions, JOptionPane.OK_CANCEL_OPTION);
            if (result != JOptionPane.OK_OPTION) {
                return -1;
            }
            int answer = 0;
            if (r1.isSelected()) {
                answer = 0;
            } else if (r2.isSelected()) {
                answer = 1;
            } else if (r3.isSelected()) {
                answer = 2;
            } else if (r4.isSelected()) {
                answer = 3;
            }
            JOptionPane.showMessageDialog(null, "Correct Answer is ' " + question.getChoices().get(question.getCorrectAnswer()) + " '", "ST-Comm", JOptionPane.INFORMATION_MESSAGE);

            return answer;
        }
    }

