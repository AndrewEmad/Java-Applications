    private void signUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signUpActionPerformed
        String sdate = jComboBox1.getSelectedItem().toString() + "/" + jComboBox2.getSelectedItem().toString() + "/" + jComboBox3.getSelectedItem().toString();

        DateFormat d = new SimpleDateFormat("dd/MM/yyyy");
        Date date = new Date();
        try {
            date = d.parse(sdate);
        } catch (ParseException ex) {
            Logger.getLogger(SignUpForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        if (jRadioButton1.isSelected()) {
            int err = StudentController.createAccount(jTextField1.getText(),
                    date, jComboBox4.getSelectedItem().toString(), jTextField2.getText(),
                    jComboBox5.getSelectedItem().toString(), String.valueOf(jPasswordField1.getPassword()));
            if (err == -1) {
                JOptionPane.showMessageDialog(null, "Registration Completed Succefully, Thank You For Choosing ST-Comm !", "Sign Up", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, errMsg[err], "Error", JOptionPane.ERROR_MESSAGE);

            }
        } else if (jRadioButton2.isSelected()) {
            int err = TeacherController.createAccount(jTextField1.getText(),
                    date, jComboBox4.getSelectedItem().toString(), jTextField2.getText(),
                    jComboBox5.getSelectedItem().toString(), String.valueOf(jPasswordField1.getPassword()));
            if (err == -1) {
                JOptionPane.showMessageDialog(null, "Thank You For Choosing ST-Comm ! Your account is Pending Confirmation !", "Sign Up", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, errMsg[err], "Error", JOptionPane.ERROR_MESSAGE);

            }
        }
    }//GEN-LAST:event_signUpActionPerformed

